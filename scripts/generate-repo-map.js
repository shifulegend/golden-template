#!/usr/bin/env node
/**
 * generate-repo-map.js
 * Generates docs/ai/repo-map.md — a compact, tree-sitter-based symbol map of the repository.
 * Pattern reference: Aider's repo-map (https://aider.chat/docs/repomap.html)
 *
 * WHY THIS EXISTS (see docs/ai/context-efficiency.md, Section 2):
 * Agents waste tokens re-scanning the codebase every session. This script generates a
 * compact Markdown map of files + their key exported symbols (functions, classes, types)
 * ONCE, checked into version control, so agents get instant orientation without a live scan.
 *
 * USAGE:
 *   node scripts/generate-repo-map.js
 * Regenerate whenever file structure changes materially. Do NOT hand-edit the output —
 * that reintroduces staleness and defeats the purpose of this file.
 *
 * IMPLEMENTATION NOTE FOR AGENTS:
 * This is a scaffold. Wire in the actual tree-sitter parser for this repo's language(s)
 * (e.g. tree-sitter-javascript, tree-sitter-python, tree-sitter-typescript) and populate
 * the `extractSymbols()` function below. Keep output token-budget-aware — do not dump full
 * function bodies, only signatures + one-line purpose per symbol, mirroring Aider's approach
 * of ranking files by dependency-graph importance and fitting to a token budget (~1k tokens
 * default) rather than exporting everything unconditionally.
 */

const fs = require('fs');
const path = require('path');

const OUTPUT_PATH = path.join(__dirname, '..', 'docs', 'ai', 'repo-map.md');
const IGNORE_DIRS = new Set(['node_modules', '.git', 'dist', 'build', '.next', 'coverage', 'docs']);

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (IGNORE_DIRS.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, files);
    else if (/\.(js|ts|jsx|tsx|py)$/.test(entry.name)) files.push(full);
  }
  return files;
}

function extractSymbols(filePath) {
  // TODO: replace with actual tree-sitter parsing for this repo's language(s).
  // Placeholder: naive regex extraction of top-level function/class names as a starting point.
  const content = fs.readFileSync(filePath, 'utf8');
  const symbols = [];
  const fnRegex = /(?:export\s+)?(?:function|class|const)\s+([A-Za-z0-9_]+)/g;
  let match;
  while ((match = fnRegex.exec(content)) !== null) {
    symbols.push(match[1]);
  }
  return symbols;
}

function generate() {
  const root = path.join(__dirname, '..');
  const files = walk(root);
  const lines = [
    '# Repository Map',
    '<!-- AUTO-GENERATED by scripts/generate-repo-map.js — do not hand-edit -->',
    `<!-- Generated: ${new Date().toISOString()} -->`,
    '',
    'Compact symbol map for instant codebase orientation. See `docs/ai/context-efficiency.md`',
    'for why this file exists and how it should be used.',
    '',
  ];
  for (const file of files) {
    const rel = path.relative(root, file);
    const symbols = extractSymbols(file);
    if (symbols.length === 0) continue;
    lines.push(`## ${rel}`);
    lines.push(symbols.map(s => `- \`${s}\``).join('\n'));
    lines.push('');
  }
  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, lines.join('\n'));
  console.log(`Repo map written to ${OUTPUT_PATH}`);
}

generate();
